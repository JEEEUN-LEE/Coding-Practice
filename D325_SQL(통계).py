# ===========================================
# 로지스틱 함수 (Logistic Function)
# ===========================================

# 로지스틱 함수는 시그모이드 함수(sigmoid function)로,
# 연속적인 값을 0~1 사이의 확률로 변환할 때 사용된다.

# 수식:
# f(x) = 1 / (1 + e^(-x))

import math

def logistic(x):
    return 1 / (1 + math.exp(-x))

print(logistic(0))   # 0.5
print(logistic(2))   # 약 0.88
print(logistic(-2))  # 약 0.12
